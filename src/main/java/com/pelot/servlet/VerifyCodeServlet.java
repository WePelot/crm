package com.pelot.servlet;

import com.pelot.utils.VerifyCodeUtil;

import java.io.IOException;

import javax.servlet.Servlet;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

/**
 * 图形验证码的生成
 *
 * @author hongcj
 * @version V1.0
 * @since 2017-10-19 15:00
 */
@WebServlet(name = "verifyCodeServlet", urlPatterns = "/servlet/verifyCodeServlet")
public class VerifyCodeServlet extends HttpServlet implements Servlet {
    static final long serialVersionUID = 1L;

    @Override
    public void service(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        response.setHeader("Pragma", "No-cache");
        response.setHeader("Cache-Control", "no-cache");
        response.setDateHeader("Expires", 0);
        response.setContentType("image/jpeg");

        //生成随机字串
        String verifyCode = VerifyCodeUtil.generateVerifyCode(4);
        //存入会话session
        HttpSession session = request.getSession(true);
        //删除以前的
        session.removeAttribute("verCode");
        session.setAttribute("verCode", verifyCode.toLowerCase());
        //生成图片
        int w = 100, h = 30;
        VerifyCodeUtil.outputImage(w, h, response.getOutputStream(), verifyCode);
    }
}
